class DiffBase:
    def __init__(self, conf, eps_model, n_steps: int) -> None:
        self.conf = conf
        self.eps_model = eps_model
        self.beta = torch.linspace(0.0001, 0.02, n_steps).to(conf.device) # Why 0.0001 to 0.02?
        self.alpha = 1. - self.beta
        self.alpha_bar = torch.cumprod(self.alpha, dim=0)
        self.n_steps = n_steps
        self.sigma2 = self.beta


